def createInitSet(dataSet):
    retDict = {}
    #好好理解@句：假O有集合{z,x,y}和{y,z,x}，那N在FP渲校相同只表示一次
    #因此第一先用字典表示，不是 += 1，是 = 1。在createTree()中也υ素M行排序
