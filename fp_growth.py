#作用：o定元素生成一l件模式基
#入：o定元素，c
#出：l件模式基字典
def findPrefixPath(basePat, treeNode):
    condPats = {}#l件模式基字典
    while treeNode != None:
        prefixPath = []#basePat父c的名Q
        ascendTree(treeNode, prefixPath)#算它的父c
        if len(prefixPath) > 1:#第一treeNode本身，第二u是它的父c
            condPats[frozenset(prefixPath[1:])] = treeNode.count
        treeNode = treeNode.nodeLink#c接的下一c
    return condPats

